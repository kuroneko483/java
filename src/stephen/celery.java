       this.id = Integer.parseInt(txtid.getText());
       this.fname = txtfirst.getText();
       this.lname = txtlast.getText();
       this.salary = slary();
       this.rank = rank();
       this.department = dep.getText();
       this.position = pos.getText();
       try{
           Class.forName("com.mysql.jdbc.Driver");
           try (Connection con = DriverManager.getConnection(
           "jdbc:mysql://localhost/stephen","root", "")){
           Statement st = con.createStatement();
          
           String sql = "insert into employee values (" + this.id + ",'" + this.fname + "','" +
           this.lname + "'," + this.rank + ",'" + this.position + "','" + this.department +
           "'," + this.salary + ")";
           
           st.executeUpdate(sql);
           con.close();
       }
       }catch(Exception e){System.out.print(e);}
       txtfirst.setText("");
       txtlast.setText("");
       cmbrank.setSelectedIndex(0);
       pos.setText("");
