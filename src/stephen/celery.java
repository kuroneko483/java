    private float slary(){
        int sal = cmbrank.getSelectedIndex();
        if(sal == 0)
        {
            this.salary = 25000;
        }
        if(sal == 2)
        {
            this.salary = 15000;
        }
        if(sal == 3)
        {
            this.salary = 12000;
        }
        return this.salary;
    }
    private int rank()
    {
        int ran = cmbrank.getSelectedIndex();
        if(ran == 0)
        {
            this.rank = 1;
        }
        if(ran == 1)
        {
            this.rank = 2;
        }
        if(ran == 2)
        {
            this.rank = 3;
        }
        return this.rank;
    }
